package Model;

public class Civilian {
        private int HealthID, DistrictID;
        private String Name, MobileNo, Address, DOB, EmergencyNo, Photo, AadharImage, Email, Religion, Diet, MaritalStatus;   

    public String getAadharImage() {
        return AadharImage;
    }

    public void setAadharImage(String AadharImage) {
        this.AadharImage = AadharImage;
    }

    public String getMaritalStatus() {
        return MaritalStatus;
    }

    public void setMaritalStatus(String MaritalStatus) {
        this.MaritalStatus = MaritalStatus;
    }

        
    public int getHealthID() {
        return HealthID;
    }

    public void setHealthID(int HealthID) {
        this.HealthID = HealthID;
    }

    public int getDistrictID() {
        return DistrictID;
    }

    public void setDistrictID(int DistrictID) {
        this.DistrictID = DistrictID;
    }

    public String getName() {
        return Name;
    }

    public void setName(String Name) {
        this.Name = Name;
    }

    public String getMobileNo() {
        return MobileNo;
    }

    public void setMobileNo(String MobileNo) {
        this.MobileNo = MobileNo;
    }

    public String getAddress() {
        return Address;
    }

    public void setAddress(String Address) {
        this.Address = Address;
    }

    public String getDOB() {
        return DOB;
    }

    public void setDOB(String DOB) {
        this.DOB = DOB;
    }

    public String getEmergencyNo() {
        return EmergencyNo;
    }

    public void setEmergencyNo(String EmergencyNo) {
        this.EmergencyNo = EmergencyNo;
    }

    public String getPhoto() {
        return Photo;
    }

    public void setPhoto(String Photo) {
        this.Photo = Photo;
    }

    public String getEmail() {
        return Email;
    }

    public void setEmail(String Email) {
        this.Email = Email;
    }

    public String getReligion() {
        return Religion;
    }

    public void setReligion(String Religion) {
        this.Religion = Religion;
    }

    public String getDiet() {
        return Diet;
    }

    public void setDiet(String Diet) {
        this.Diet = Diet;
    }

     
        
}
