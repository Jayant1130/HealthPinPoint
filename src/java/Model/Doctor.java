package Model;

public class Doctor {
    int DoctorID;
    String Spe_in, ClinicProof, DegreeProof, HealthID;

    public int getDoctorID() {
        return DoctorID;
    }

    public void setDoctorID(int DoctorID) {
        this.DoctorID = DoctorID;
    }

    public String getSpe_in() {
        return Spe_in;
    }

    public void setSpe_in(String Spe_in) {
        this.Spe_in = Spe_in;
    }

    public String getClinicProof() {
        return ClinicProof;
    }

    public void setClinicProof(String ClinicProof) {
        this.ClinicProof = ClinicProof;
    }

    public String getDegreeProof() {
        return DegreeProof;
    }

    public void setDegreeProof(String DegreeProof) {
        this.DegreeProof = DegreeProof;
    }

    public String getHealthID() {
        return HealthID;
    }

    public void setHealthID(String HealthID) {
        this.HealthID = HealthID;
    }
            
}
